public class pattren {

    public static void main(String[] args) {
        float num1 = 53.5f / 2;
        float num2 = 53.5f % 2;
        System.out.println("53.5f / 2:" + num1);
        System.out.println("53.5f % 2:" + num2);
        System.out.println(" \n");

        // 2.-> 54/2.0
        float num3 = 54.0f / 2;
        float num4 = 54.0f % 2;
        System.out.println("54.0f / 2:" + num3);
        System.out.println("54.0f % 2:" + num4);
        System.out.println(" \n");

        // 4.=> 32.0/2 and 32.0%2.0
        float num5 = 32.0f / 2;
        float num6 = 32.0f % 2;
        System.out.println("32.0f / 2:" + num5);
        System.out.println("32.0f % 2:" + num6);
        System.out.println(" \n");

        // 5.=> 38.0/2.5 and 38.0%2.5
        float num7 = 38.0f / 2.5f;
        float num8 = 38.0f % 2.5f;
        System.out.println("38.0f / 2.5f:" + num7);
        System.out.println("38.0f % 2.5f:" + num8);
        System.out.println(" \n");

        // 6. --> 5371%10
        int num9 = 5371 % 10;
        System.out.println("5371 % 10:" + num9);
        // 7. --> 5371/10
        int num10 = 5371 / 10;
        System.out.println("5371 / 10:" + num10);
        // 8. --> 537%10
        int num11 = 537 % 10;
        System.out.println("537 % 10:" + num11);
        // 9. --> 537/10
        int num12 = 537 / 10;

        System.out.println("537 / 10:" + num12);
        // 10. --> 53%10
        int num13 = 53 % 10;
        System.out.println("53 % 10:" + num13);
        // 11. --> 53/10
        int num14 = 53 / 10;
        System.out.println("53 / 10:" + num14);

        // 12. --> 5%10
        int num15 = 5 % 10;
        System.out.println("5 % 10:" + num15);
        // 13. --> 5/10
        int num16 = 5 / 10;
        System.out.println("5 / 10:" + num16);
        // 14. --> 5%0
        int num17 = 5 % 0;
        System.out.println("5 % 0:" + num17);
        // 15. --> 5/0

        int num18 = 5 / 0;
        System.out.println("5 / 0:" + num18);

    }
}
