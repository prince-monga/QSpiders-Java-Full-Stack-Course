public class power {
    public static void main(String[] args) {
        // 9. power 4th power of 5
        int base = 5;

        int pow1 = base * base * base * base;
        System.out.println("4th power of 5 is: " + pow1);

        // 10. power 6th power of 3
        int base2 = 3;
        int pow2 = base2 * base2 * base2 * base2 * base2 * base2;
        System.out.println("6th power of 3 is: " + pow2);

        // 11. 2nd power of 6
        int base3 = 6;

        int pow3 = base3 * base3;
        System.out.println("2nd power of 6 is: " + pow3);

        // 12. 3rd power of 8
        int base4 = 8;
        int pow4 = base4 * base4 * base4;
        System.out.println("3rd power of 8 is: " + pow4);
    }
}
