public class SqCube {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 7;
        int num3 = 9;

        int square1 = num1 * num1;
        int square2 = num2 * num2;
        int square3 = num3 * num3;
        System.out.println("Square of " + num1 + " is: " + square1);
        System.out.println("Square of " + num2 + " is: " + square2);
        System.out.println("Square of " + num3 + " is: " + square3);

        // 9. cube
        int c1 = 5;
        int c2 = 2;
        int c3 = 15;
        int cube1 = c1 * c1 * c1;
        int cube2 = c2 * c2 * c2;
        int cube3 = c3 * c3 * c3;
        System.out.println("Cube of " + c1 + " is: " + cube1);
        System.out.println("Cube of " + c2 + " is: " + cube2);
        System.out.println("Cube of " + c3 + " is: " + cube3);

    }
}
